package com.minsait.onesait.platform.config.versioning;

import org.yaml.snakeyaml.constructor.AbstractConstruct;
import org.yaml.snakeyaml.constructor.SafeConstructor;
import org.yaml.snakeyaml.nodes.Node;
import org.yaml.snakeyaml.nodes.ScalarNode;
import org.yaml.snakeyaml.nodes.Tag;

public class HTMLConstructor extends SafeConstructor {
	public HTMLConstructor() {
		yamlConstructors.put(new Tag("!html"), new YamlHTMLConstructor());
	}

	private class YamlHTMLConstructor extends AbstractConstruct {
		@Override
		public Object construct(Node node) {
			final String val = (String) constructScalar((ScalarNode) node);
			return new HTML(val);
		}
	}
}
