package com.minsait.onesait.platform.controlpanel.controller.monitoring;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.access.prepost.PreAuthorize;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;

import com.minsait.onesait.platform.resources.service.IntegrationResourcesService;
import com.minsait.onesait.platform.resources.service.IntegrationResourcesServiceImpl.Module;
import com.minsait.onesait.platform.resources.service.IntegrationResourcesServiceImpl.ServiceUrl;

@Controller
@RequestMapping("monitoringui")
@PreAuthorize("@securityService.hasAnyRole('ROLE_ADMINISTRATOR')")
public class MonitoringUIController {
	@Autowired
	private IntegrationResourcesService resourcesService;

	@GetMapping
	public String iframe(Model model) {
		model.addAttribute("url", getMonitoringUrl());
		return "monitoring/ui";
	}

	private String getMonitoringUrl() {
		return resourcesService.getUrl(Module.MONITORINGUI, ServiceUrl.EMBEDDED);
	}
}
