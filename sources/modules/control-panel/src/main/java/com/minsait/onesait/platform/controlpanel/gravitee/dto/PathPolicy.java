package com.minsait.onesait.platform.controlpanel.gravitee.dto;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@JsonInclude(Include.NON_NULL)
public class PathPolicy {

	@Builder.Default
	private String[] methods = new String[] { "GET", "POST", "PUT", "DELETE", "HEAD", "PATCH", "OPTIONS", "TRACE",
			"CONNECT" };
	private boolean enabled;
	private String description;
	@JsonProperty(TransformHeadersPolicy.TRANSFORM_HEADERS)
	private TransformHeadersPolicy policy;
	private GroovyPolicy groovy;

	public JsonNode toJsonNode() {
		final ObjectMapper mapper = new ObjectMapper();
		return mapper.valueToTree(this);
	}

}
